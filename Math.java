public class Math {

    public int add(int a,int b){
        return a+b;
    }

    public int sub(int a,int b){
        return a-b;
    }

    public int mult(int a,int b){
        return a*b;
    }

    public int div(int a,int b){
        return a/b;
    }
    public int sq(int a){
        return a*a;
    }

    public int cube(int a){
        return a*a*a;
    }

}
